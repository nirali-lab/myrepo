# --- Robust CV with per-fold label remapping ---
def evaluate_cv(X, y, model_builder, n_splits=3, epochs=10, batch_size=64, verbose=0):
    skf = StratifiedKFold(n_splits=n_splits, shuffle=True, random_state=42)
    accs, f1s, cms = [], [], []
    fold = 0

    for train_idx, test_idx in skf.split(X.reshape(X.shape[0], -1), y):
        fold += 1
        X_train, X_test = X[train_idx], X[test_idx]
        y_train_global, y_test_global = y[train_idx], y[test_idx]

        # ⚙️ Re-map labels inside this fold to local indices (0..n_classes-1)
        le_fold = LabelEncoder()
        y_train = le_fold.fit_transform(y_train_global)
        y_test = np.array([le_fold.transform([yy])[0] if yy in le_fold.classes_ else -1 for yy in y_test_global])

        # Drop samples in test that have unseen labels
        valid_idx = y_test != -1
        X_test, y_test = X_test[valid_idx], y_test[valid_idx]

        n_classes = len(le_fold.classes_)
        if n_classes < 2:
            print(f"⚠️ Skipping fold {fold}: only {n_classes} class found in training set.")
            continue

        print(f"\n🔹 Fold {fold}: training on {n_classes} unique classes")

        # Build and train model
        model = model_builder(X_train.shape[1:], n_classes)
        cb = [callbacks.EarlyStopping(patience=3, restore_best_weights=True)]
        model.fit(X_train, y_train, validation_split=0.1,
                  epochs=epochs, batch_size=batch_size,
                  callbacks=cb, verbose=verbose)

        # Predict & evaluate
        preds = model.predict(X_test)
        y_pred = np.argmax(preds, axis=1)

        acc = accuracy_score(y_test, y_pred)
        f1 = f1_score(y_test, y_pred, average='weighted')
        cm = confusion_matrix(y_test, y_pred)

        print(f"✅ Fold {fold}: acc={acc:.4f}, f1={f1:.4f}, classes={n_classes}")
        accs.append(acc)
        f1s.append(f1)
        cms.append(cm)

    if len(accs) == 0:
        raise RuntimeError("❌ No valid folds completed. Check dataset class balance or labels.")

    return {
        'accs': accs,
        'f1s': f1s,
        'cms': cms,
        'acc_mean': np.mean(accs),
        'f1_mean': np.mean(f1s)
    }


