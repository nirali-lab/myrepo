def smooth_ecg(data, window=15, poly=3):
    """Apply Savitzky-Golay filter and z-score normalization per sample."""
    data = np.asarray(data)
    smoothed = np.array([savgol_filter(x, window, poly) for x in data])
    smoothed = (smoothed - np.mean(smoothed, axis=1, keepdims=True)) / (
        np.std(smoothed, axis=1, keepdims=True) + 1e-8
    )
    return smoothed.astype(np.float32)

def rescale_to_real_range(fake_data, real_data):
    """
    Rescale synthetic samples so each sample matches the (min,max) of corresponding real sample.
    If shapes differ, falls back to global min/max matching.
    """
    fake = np.array(fake_data)
    real = np.array(real_data)
    if fake.shape == real.shape:
        out = np.empty_like(fake)
        for i in range(len(fake)):
            rmin, rmax = np.min(real[i]), np.max(real[i])
            fmin, fmax = np.min(fake[i]), np.max(fake[i])
            if fmax - fmin < 1e-8:
                out[i] = np.ones_like(fake[i]) * rmin
            else:
                out[i] = (fake[i] - fmin) / (fmax - fmin) * (rmax - rmin) + rmin
        return out
    else:
        rmin, rmax = np.min(real), np.max(real)
        fmin, fmax = np.min(fake), np.max(fake)
        if fmax - fmin < 1e-8:
            return np.ones_like(fake) * rmin
        return (fake - fmin) / (fmax - fmin) * (rmax - rmin) + rmin

