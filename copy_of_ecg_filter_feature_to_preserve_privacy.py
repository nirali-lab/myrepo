def run_cv(X, y, model_fn, n_splits=3, epochs=15, batch_size=64):
    skf = StratifiedKFold(n_splits=n_splits, shuffle=True, random_state=42)
    accs, f1s = [], []

    for fold, (train_idx, test_idx) in enumerate(skf.split(X.reshape(X.shape[0], -1), y), 1):
        X_train, X_test = X[train_idx], X[test_idx]
        y_train, y_test = y[train_idx], y[test_idx]

        model = model_fn(X_train.shape[1:], len(np.unique(y_train)))

        cb = [
            callbacks.EarlyStopping(patience=3, restore_best_weights=True),
            callbacks.ReduceLROnPlateau(factor=0.5, patience=2)
        ]

        model.fit(X_train, y_train, validation_split=0.1,
                  epochs=epochs, batch_size=batch_size,
                  verbose=1, callbacks=cb)

        y_pred = np.argmax(model.predict(X_test, verbose=0), axis=1)
        acc = accuracy_score(y_test, y_pred)
        f1 = f1_score(y_test, y_pred, average='weighted')
        accs.append(acc)
        f1s.append(f1)
        print(f"🔹 Fold {fold}: acc={acc:.4f}, f1={f1:.4f}")

    print(f"\n📊 Mean Accuracy: {np.mean(accs):.4f}, Mean F1: {np.mean(f1s):.4f}")
    return np.mean(accs), np.mean(f1s)

# =============================================
# 🧠 GAN: Generator + Discriminator
# =============================================

def build_generator(input_len=250, noise_dim=100):
    real_input = layers.Input(shape=(input_len,))
    noise_input = layers.Input(shape=(noise_dim,))
    x = layers.Concatenate()([real_input, noise_input])
    x = layers.Dense(512, activation='relu')(x)
    x = layers.Dense(512, activation='relu')(x)
    x = layers.Dense(input_len, activation='tanh')(x)
    return models.Model([real_input, noise_input], x, name="Generator")

def build_discriminator(input_len=250):
    ecg_input = layers.Input(shape=(input_len,))
    x = layers.Dense(256, activation='relu')(ecg_input)
    x = layers.Dense(128, activation='relu')(x)
    x = layers.Dense(1, activation='sigmoid')(x)
    return models.Model(ecg_input, x, name="Discriminator")

# =============================================
# ⚔️ GAN LOSSES
# =============================================

def smoothness_loss(x):
    return tf.reduce_mean(tf.square(x[:, 1:] - x[:, :-1]))

bce = tf.keras.losses.BinaryCrossentropy(from_logits=False)

def generator_loss(fake_output, fake_signal, lambda_smooth=0.5):
    adv_loss = bce(tf.ones_like(fake_output), fake_output)
    smooth_loss = smoothness_loss(fake_signal)
    return adv_loss + lambda_smooth * smooth_loss

def discriminator_loss(real_output, fake_output):
    real_loss = bce(tf.ones_like(real_output), real_output)
    fake_loss = bce(tf.zeros_like(fake_output), fake_output)
    return real_loss + fake_loss

# =============================================
# 🚀 TRAIN GAN
# =============================================

def train_gan(X_real, epochs=1000, batch_size=64, noise_dim=100, print_every=100):
    gen = build_generator(X_real.shape[1], noise_dim)
    disc = build_discriminator(X_real.shape[1])
    gen_opt = optimizers.Adam(1e-4)
    disc_opt = optimizers.Adam(1e-4)

    X_real = tf.convert_to_tensor(X_real, dtype=tf.float32)

    for epoch in range(epochs):
        idx = np.random.randint(0, X_real.shape[0], batch_size)
        real_batch = tf.gather(X_real, idx)
        noise = tf.random.normal([batch_size, noise_dim])

        with tf.GradientTape() as g_tape, tf.GradientTape() as d_tape:
            fake_data = gen([real_batch, noise], training=True)
            real_output = disc(real_batch, training=True)
            fake_output = disc(fake_data, training=True)

            g_loss = generator_loss(fake_output, fake_data)
            d_loss = discriminator_loss(real_output, fake_output)

        grads_g = g_tape.gradient(g_loss, gen.trainable_variables)
        grads_d = d_tape.gradient(d_loss, disc.trainable_variables)
        gen_opt.apply_gradients(zip(grads_g, gen.trainable_variables))
        disc_opt.apply_gradients(zip(grads_d, disc.trainable_variables))

        if epoch % print_every == 0:
            print(f"Epoch {epoch}: GenLoss={g_loss:.4f}, DiscLoss={d_loss:.4f}")

    return gen

